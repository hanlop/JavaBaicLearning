package com.oracle;

//字符相加案例
public class Demo02 {
    public static void main(String[] args) {
        //单独打印,展示字符
        System.out.println('A');
        System.out.println('B');
        System.out.println('C');

        //参与运算展示数值
        System.out.println('A'+1);
        System.out.println('B'+1);
        System.out.println('C'+1);

    }
}
