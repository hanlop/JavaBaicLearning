package com.oracle;

public class Demo01 {
    //打印 一到五 和五到一 使用循环
    public static void main(String[] args) {
        for(int i=1;i<=5;i++)
        {
            System.out.println(i);
        }
        System.out.println("----------------------------");
        for(int i=5;i>=1;i--)
        {
            System.out.println(i);
        }
    }
}
